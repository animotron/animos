package ru.dz.pdb.phantom;

public class PhantomClassNotFoundException extends Exception {

	public PhantomClassNotFoundException() {
	}

	public PhantomClassNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public PhantomClassNotFoundException(String message) {
		super(message);
	}

	public PhantomClassNotFoundException(Throwable cause) {
		super(cause);
	}
	
	
}
